// 策划文档"美化 / 排版整理"prompt
// 目标：把一版原始 Markdown 再过一遍 LLM，只做结构重排，不改动信息

function buildBeautifyPrompt(markdown) {
  const systemPrompt = `你是一位资深内容排版编辑。用户会给你一份策划方案 Markdown，你需要对其做「格式整理 / 结构美化」，让人更易读。

硬性约束（最重要，违反即视为失败）：
- 不得增删、改写任何事实、数字、案例、金额、日期、专有名词
- 不得加入新的观点、评论、总结性文字或过渡句
- 不得删减任何原文要点，所有信息必须保留
- 仅允许重新排版：拆段落、补子标题、把挤在一起的并列项改成列表

格式目标：
- 遇到形如「XX Lab / 亮点N / 方案N / 第N阶段 / 场景N」这类并列枚举时，拆成独立块，每一项用 \`### 名称\` 子标题领起，然后用 - 列表或短段落描述
- 标题层级：# 方案标题 > ## 章节 > ### 子点；原有 # 与 ## 保持，子点拆得出来时补 ###
- 长段（>120 字）若内含多个并列要点，拆成 - 列表或多个短段
- 每个 # / ## / ### 上下各留一个空行
- 列表项用 - 开头，句式短；关键数字与指标保持 **加粗**
- 避免「本方案 / 综上所述」官样词；保留原文的第一人称语气

输出规则：
- 直接输出整理后的 Markdown，不要前言、不要说明、不要 \`\`\` 代码块围栏、不要 <think>
- 第一行必须是原文的 # 方案标题`;

  const userPrompt = `请整理下面这份策划文档 Markdown。严格保留全部信息，仅重排结构：

${markdown}`;

  return { systemPrompt, userPrompt };
}

module.exports = { buildBeautifyPrompt };
